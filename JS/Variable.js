//We can change the value of the variable using let keyword.
//we cannot redeclare the same variable. 
let myName="divish"
myName="Divi"
console.log(myName)

let dog = { name : 'Spot', breed : 'Dalmatian'}
console.log(dog.name)

//using var is a global variable
